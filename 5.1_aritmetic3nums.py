#!/usr/bin/env python

# perform arithmatic operation on two number

a = int(input(f"Enter one  number: "))
b = int(input(f"Enter 2nd number: "))
c = int(input(f"Enter 3th number: "))

print(f"Addition of two numer = {str(a+b+c)}")
print(f"Subtraction of two number = {str(a-b-c)}")
print(f"Multiplication of two number = {str(a*b*c)}")
print(f"Division of two number = {str(a/b/c)}")
print(f"Modulus of two number = {str(a%b%c)}")

